function consoleWrite(){
	if(event.key === "Enter") {
		let written = commandLine.value.toLowerCase();
		
		if (written === "0" || written === "home") {		
			homeSelect();
		} else if (written === "1" || written === "about") {		
			aboutSelect();
		} else if (written === "2" || written === "works") {			
			worksSelect();
		} else if (written === "3" || written === "skills") {			
			skillsSelect();
		} else if (written === "4" || written === "print") {			
			printSelect();
		} else if (written === "5" || written === "help") {			
			helpSelect();
		} else {			
			error();
		}
	}
}

